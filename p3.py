import pandas as pd
import numpy as np

# Создаем DataFrame с данными об учениках и их оценках
data = {
    'Ученик': ['Иванов', 'Петров', 'Сидоров', 'Кузнецов', 'Васильев', 'Николаев', 'Смирнов', 'Федоров', 'Михайлов', 'Алексеев'],
    'Математика': [5, 4, 3, 5, 4, 3, 4, 5, 4, 3],
    'Физика': [4, 5, 4, 3, 4, 5, 3, 4, 5, 4],
    'Химия': [3, 4, 5, 4, 3, 4, 5, 3, 4, 5],
    'Литература': [4, 3, 4, 5, 4, 3, 4, 5, 3, 4],
    'История': [5, 4, 3, 4, 5, 4, 3, 4, 5, 3]
}

df = pd.DataFrame(data)

# Выводим первые несколько строк DataFrame
print("Первые строки DataFrame:")
print(df.head())
print("\n")

# Вычисляем среднюю оценку по каждому предмету
mean_grades = df.mean(numeric_only=True)
print("Средняя оценка по каждому предмету:")
print(mean_grades)
print("\n")

# Вычисляем медианную оценку по каждому предмету
median_grades = df.median(numeric_only=True)
print("Медианная оценка по каждому предмету:")
print(median_grades)
print("\n")

# Вычисляем Q1 и Q3 для оценок по математике
Q1_math = df['Математика'].quantile(0.25)
Q3_math = df['Математика'].quantile(0.75)
IQR_math = Q3_math - Q1_math

print("Квантили для оценок по математике:")
print(f"Q1 (25-й персентиль): {Q1_math}")
print(f"Q3 (75-й персентиль): {Q3_math}")
print(f"IQR (интерквартильный размах): {IQR_math}")
print("\n")

# Вычисляем стандартное отклонение по каждому предмету
std_grades = df.std(numeric_only=True)
print("Стандартное отклонение по каждому предмету:")
print(std_grades)